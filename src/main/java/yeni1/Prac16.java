package yeni1;

public class Prac16 {
    public static void main(String[] args) {
        //Loops    çok important döngüler
        //kod parçalarını kural döngüsü içinde tekrarlar
        //for-while-dowhile -for each
        //--------------------------------
        //4 kere hi...! yazdır
        //for (başlangıç;loop çalışma;artırma eksiltme){
        // }
        for (int i=1;i<5;i++){
            System.out.println("hi...!");
        }
        //----------------------------------
        //20 den 3e kadar tümsayılar
        for (int i=20;i>2;i--){
            System.out.print(i+" ");
        }
        System.out.println("");

        //---------------------------------------
        // 3 den 20 ye çift yazdır
        for (int i=3;i<21;i++){
            if (i%2==0){
                System.out.print(i+" ");
            }
        }



        







    }
}
