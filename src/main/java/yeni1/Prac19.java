package yeni1;

public class Prac19 {
    public static void main(String[] args) {
        //6 dan 10 a kadar toplamı console a yazdır
        //678910
        int sum=0;
        for (int i = 6; i <11 ; i++) {
            sum=sum+i;
        }
        System.out.println("sum = " + sum);
        //---------------------------------------------
        //10 dan 7 ye kadar çarpımı yaz
        int multiple=1;
        for (int i = 10; i >6 ; i--) {
            multiple=multiple*i;
        }
        System.out.println("multiple = " + multiple);
        //--------------------------------------------






    }
}

